# ╔═╡ c7ed34fa-b393-4221-936a-26798a6b3ed2
md"
### Método de Euler

| $x$      | $y$      | $\frac{dy}{dx}$ | $\frac{dy}{dx} h + y$ |
|:--------:|:--------:|:---------------:|:---------------------:|
| $x_0$    | $y_0$    |                 |                       |
| $\vdots$ | $\vdots$ | $\vdots$        | $\vdots$              |
| $x_n$    | $y_n$    |                 |                       |

donde

```math
Y_n = Y_{n - 1} + h F \left( x_{n - 1}, y_{n - 1} \right)
```

!!! note
    Debemos asegurarnos de que nuestro incremento $h$ no sea demasiado grande o nuestra solución numérica será inexacta.
"

# ╔═╡ c34ec2e2-8207-4c0e-86ac-9e88874417d5
function euler(lim :: Real, f :: Function) :: Float64
	h = 0.0001

	t = 0
	x = 0
	y = 0
	yd = f(t, x, y)

	while x < lim
		t += h
		x += y * h
		y += yd * h
		yd = f(t, x, y)
	end

	return t
end

