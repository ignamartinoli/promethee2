- la tasa de llegada de camiones es de 1 cada entre 5 y 9 horas con distribución uniforme;
- la tasa de descarga se rige por la ecuación
```math
\left.
	\frac{d^2 D}{d t^2} =
		4 \left(\frac{dD}{dt}\right)^2 + 6D + 8t \quad
