function simulation(factory, silos, camions, filling, events, state)
	for _ in 1:n
		time, event = minimum(events)

		if event == Inicio
			factory.silo = first(silos)
			factory.silo.supplying = true

			events[time + arrival()] = Llegada
			events[time + stocked(factory)] = Abastecido
		elseif event == Llegada
			camion = Camion(quantity(), nothing)

			events[time + arrival()] = Llegada

			if any(getproperty.(silos, :filling)) push!(camions, camion)
			else
				fillable = filter(silo -> 20 - silo.quantity != 0 && !silo.supplying, silos)
				
				if !isempty(fillable)
					filling = camion
					filling.silo = first(fillable)
					filling.silo.filling = true

					diff = filling.silo.quantity + filling.quantity
					events[time + filled(diff > 20 ? 20 : diff)] = Llenado
				else
					push!(camions, camion)
				end
			end
		elseif event == Llenado
			filling.silo.quantity = 20
			filling.silo.filling = false

			prep = 1 / 6
			
			if filling.quantity == 0 && !isempty(camions)
				filling = pop!(camions)
				prep = 0
			end
			
			fillable = filter(silo -> 20 - silo.quantity != 0 && !silo.supplying, silos)

			if !isempty(fillable)
				filling.silo = first(fillable)
				filling.silo.filling = true
				
				diff = filling.silo.quantity + filling.quantity
				events[time + prep + filled(diff > 20 ? 20 : diff)] = Llenado
			end
		elseif event == Abastecido
			factory.silo.quantity = 0
			factory.silo.supplying = false

			usable = filter(silo -> silo.quantity != 0 && !silo.filling, silos)

			if !isempty(usable)
				factory.silo = first(usable)
				factory.silo.supplying = true

				events[time + stocked(factory)] = Abastecido
			end
		end

		push!(
			state,
			(
				time,
				string(event),
				findfirst(silo -> silo.filling, silos),
				findfirst(silo -> silo.supplying, silos),
				silos[1].quantity, silos[2].quantity, silos[3].quantity, silos[4].quantity,
				length(camions)
			)
		)

		delete!(events, time)
	end
end

